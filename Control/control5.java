// Write a program to check whether a number is even or odd

public class control5 
{
    public static void main(String[] args) 
    {
        int a = 5;

        if (a%2==0) 
        {
          System.out.println(+a+ " is even");   
        } 
        else 
        {
           System.out.println(+a+ " is odd"); 
        }
    }
}
